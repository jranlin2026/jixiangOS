function hasAnyPermission(user: AuthenticatedUser, permissionKeys: readonly string[], action: 'read' | 'write'): boolean {
  return permissionKeys.some((permissionKey) => hasPermission(user, permissionKey, action));
}

export function isLegacyStorageKeyRegistered(key: string): boolean {
  return isAssetStorageKey(key) || Boolean(policies[key]);
}

export function canAccessLegacyStorageKey(
  user: AuthenticatedUser,
  key: string,
  operation: LegacyStorageOperation,
): boolean {
  if (isAssetStorageKey(key)) {
    return operation === 'write' ? canWriteStorageKey(user, key) : canReadStorageKey(user, key);
  }

  const policy = policies[key];
  if (!policy) return false;
  if (operation === 'runtime') return policy.runtime && hasAnyPermission(user, policy.read, 'read');
  if (operation === 'write' && COMMAND_ONLY_WRITE_KEYS.has(key)) return false;
  return operation === 'write'
    ? hasAnyPermission(user, policy.write, 'write')
    : hasAnyPermission(user, policy.read, 'read');
}
