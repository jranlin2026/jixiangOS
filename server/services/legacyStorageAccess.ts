export type LegacyStorageOperation = 'read' | 'write' | 'runtime';

type LegacyStoragePolicy = {
  read: readonly string[];
  write: readonly string[];
  runtime: boolean;
};

const policies: Record<string, LegacyStoragePolicy> = {};
